numero_1 = float(input("Ingrese el primer número: "))
numero_2 = float(input("Ingrese el segundo número: "))
numero_3 = float(input("Ingrese el tercer número: "))

menor = min(numero_1, numero_2, numero_3)
suma = numero_1 + numero_2 + numero_3

print("El menor de los tres números es:", menor)
print("La suma de los tres números es:", suma)
